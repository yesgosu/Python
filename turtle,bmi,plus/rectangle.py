s = input("키를 입력하세요:") # 키를 입력받는다.
if s == "R" or s =="r": # R 또는 r을 입력시
    print("rectangle")  # rectangle라는 문장이 출력된다.
if s == "T" or s =="t": # T 또는 t를 입력시,
    print("triangle")   # triangle 라는 문장이 출력된다.
if s == "C" or s =="c": # C 또는 c를 입력시,
    print("circle") # cirlce라는 문장이라고 출력된다.
else:
    print("unkown") # 그외의 문자가 들어오게되면 unkown라는 문장이 출력된다.